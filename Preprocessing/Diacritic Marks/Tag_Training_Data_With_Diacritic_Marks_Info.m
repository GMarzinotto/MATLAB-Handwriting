labeled_writers_indexes = [];

for i=1:length(labeled_writers)

    
    writer_idx = find( ismember( {Writers.Writer_ID} , labeled_writers{i} ) );
    
    if(~isnan(writer_idx))
        
        labeled_writers_indexes = [labeled_writers_indexes; writer_idx];
        kWriter                 = Writers(writer_idx);
        
        for j=1:2
            if(isfield(kWriter,strcat('Test',num2str(j))))
                jTest = eval(strcat('kWriter.Test',num2str(j),';'));
                if(isfield(jTest,'strokes_diacritic_marks') && strcmp(jTest.Tracking,'ON_TABLET') )            
                    apostrophe_stroke_idx = eval(strcat('Diacritic_Database.',labeled_writers{i},'.Test',num2str(j),'.Apostrophe;'));
                    accent_gra_stroke_idx = eval(strcat('Diacritic_Database.',labeled_writers{i},'.Test',num2str(j),'.Grave_Accent;'));
                    accent_acu_stroke_idx = eval(strcat('Diacritic_Database.',labeled_writers{i},'.Test',num2str(j),'.Acute_Accent;'));
                    circumflex_stroke_idx = eval(strcat('Diacritic_Database.',labeled_writers{i},'.Test',num2str(j),'.Circumflex;'));
                    charac_dot_stroke_idx = eval(strcat('Diacritic_Database.',labeled_writers{i},'.Test',num2str(j),'.Character_Dot;'));
                    bar_of_t_stroke_idx   = eval(strcat('Diacritic_Database.',labeled_writers{i},'.Test',num2str(j),'.T_Bar;'));
                    cedilla_stroke_idx    = eval(strcat('Diacritic_Database.',labeled_writers{i},'.Test',num2str(j),'.Cedilla;'));

                    
                    for k=1:length(apostrophe_stroke_idx)
                        jTest.strokes_diacritic_marks{apostrophe_stroke_idx(k)} = 'Apostrophe';
                    end
                    for k=1:length(accent_gra_stroke_idx)
                        jTest.strokes_diacritic_marks{accent_gra_stroke_idx(k)} = 'Grave_Accent';
                    end
                    for k=1:length(accent_acu_stroke_idx)
                        jTest.strokes_diacritic_marks{accent_acu_stroke_idx(k)} = 'Acute_Accent';
                    end
                    for k=1:length(circumflex_stroke_idx)
                        jTest.strokes_diacritic_marks{circumflex_stroke_idx(k)} = 'Circumflex';
                    end
                    for k=1:length(charac_dot_stroke_idx)
                        jTest.strokes_diacritic_marks{charac_dot_stroke_idx(k)} = 'Character_Dot';
                    end
                    for k=1:length(bar_of_t_stroke_idx)
                        jTest.strokes_diacritic_marks{bar_of_t_stroke_idx(k)} = 'T_Bar';
                    end
                    for k=1:length(cedilla_stroke_idx)
                        jTest.strokes_diacritic_marks{cedilla_stroke_idx(k)}  = 'Cedilla';
                    end
                    
                    eval(strcat('Writers(i).Test',num2str(j),' = jTest;'));
                end
            end
