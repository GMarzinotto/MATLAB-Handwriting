labeled_writers_indexes = [];

for i=1:length(labeled_writers)

    
    writer_idx = find( ismember( {Writers.Writer_ID} , labeled_writers{i} ) );
    
    if(~isnan(writer_idx))
        
        labeled_writers_indexes = [labeled_writers_indexes; writer_idx];
        kWriter                 = Writers(writer_idx);
        
        for j=1:2
            if(isfield(kWriter,strcat('Test',num2str(j))))
                jTest = eval(strcat('kWriter.Test',num2str(j),';'));
                if(isfield(jTest,'strokes_punctuation_marks') && strcmp(jTest.Tracking,'ON_TABLET') )            
                    punct_dot_stroke_idx  = eval(strcat('Punctuation_Database.',labeled_writers{i},'.Test',num2str(j),'.Punct_Dot;'));
                    punct_coma_stroke_idx = eval(strcat('Punctuation_Database.',labeled_writers{i},'.Test',num2str(j),'.Punct_Coma;'));
                    exclamatio_stroke_idx = eval(strcat('Punctuation_Database.',labeled_writers{i},'.Test',num2str(j),'.Mark_Exclam;'));
                    question_stroke_idx   = eval(strcat('Punctuation_Database.',labeled_writers{i},'.Test',num2str(j),'.Mark_Quest;'));
                    hyphen_stroke_idx     = eval(strcat('Punctuation_Database.',labeled_writers{i},'.Test',num2str(j),'.Hyphen;'));
                    parenth_stroke_idx    = eval(strcat('Punctuation_Database.',labeled_writers{i},'.Test',num2str(j),'.Parenthesis;'));
                    
                    
                    for k=1:length(punct_dot_stroke_idx)
                        jTest.strokes_punctuation_marks{punct_dot_stroke_idx(k)} = 'Punct_Dot';
                    end
                    for k=1:length(punct_coma_stroke_idx)
                        jTest.strokes_punctuation_marks{punct_coma_stroke_idx(k)} = 'Punct_Coma';
                    end
                    for k=1:length(exclamatio_stroke_idx)
                        jTest.strokes_punctuation_marks{exclamatio_stroke_idx(k)} = 'Mark_Exclam';
                    end
                    for k=1:length(question_stroke_idx)
                        jTest.strokes_punctuation_marks{question_stroke_idx(k)} = 'Mark_Quest';
                    end
                    for k=1:length(hyphen_stroke_idx)
                        jTest.strokes_punctuation_marks{hyphen_stroke_idx(k)} = 'Hyphen';
                    end
                    for k=1:length(parenth_stroke_idx)
                        jTest.strokes_punctuation_marks{parenth_stroke_idx(k)} = 'Parenthesis';
                    end
                    
                    eval(strcat('Writers(i).Test',num2str(j),' = jTest;'));
                end
            end
        end
    end
